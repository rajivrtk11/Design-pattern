package TicTacToe.generic;

public class Student {
    String name;
    String id;
    String age;
    String mobile;

    @Override
    public String toString() {
        return "Student{" +
                "name='" + name + '\'' +
                ", id='" + id + '\'' +
                ", age='" + age + '\'' +
                ", mobile='" + mobile + '\'' +
                '}';
    }
}
